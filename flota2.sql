-- flota de vehículos de empresa

CREATE SCHEMA flota;

CREATE TABLE flota.coche (
  matricula varchar(10) PRIMARY KEY NOT NULL,
  fecha_de_compra varchar(12) NOT NULL,
  kms_totales INT NOT NULL,
  id_grupo INT,
  id_revision INT,
  id_poliza INT,
  id_color INT
);

CREATE TABLE flota.grupo (
  id SERIAL PRIMARY KEY,
  grupo varchar(50) NOT NULL,
  id_marca INT NOT NULL
);

CREATE TABLE flota.marca (
  id SERIAL PRIMARY KEY,  
  marca VARCHAR(25) NOT NULL,
  id_modelo INT NOT NULL
);

CREATE TABLE flota.modelo (
  id SERIAL PRIMARY KEY,
  modelo varchar(50) NOT NULL
);

CREATE TABLE flota.poliza (
  id SERIAL PRIMARY KEY,
  poliza varchar(50) NOT null,
  id_seguro INT NOT NULL   
);

CREATE TABLE flota.seguro (
  id SERIAL PRIMARY KEY,
  seguro varchar(50) NOT NULL
);

CREATE TABLE flota.revision (
  id SERIAL PRIMARY KEY,
  revision_km INT NOT NULL,
  fecha_revision varchar(12) NOT NULL,
  precio_revision FLOAT NOT NULL,
  id_divisas INT NOT NULL
);

CREATE TABLE flota.divisas (
  id SERIAL PRIMARY KEY,
  divisas VARCHAR(15) NOT NULL
);

create table flota.color (
id SERIAL PRIMARY key,
color varchar(50) NOT NULL
);


ALTER TABLE flota.coche ADD CONSTRAINT fk_color_coche FOREIGN key (id_color) REFERENCES flota.color(id);
ALTER TABLE flota.coche ADD CONSTRAINT fk_poliza_coche FOREIGN key (id_poliza) REFERENCES flota.poliza(id);
ALTER TABLE flota.coche ADD CONSTRAINT fk_revision_coche FOREIGN key (id_revision) REFERENCES flota.revision(id);
ALTER TABLE flota.coche ADD CONSTRAINT fk_grupo_coche FOREIGN key (id_grupo) REFERENCES flota.grupo(id);
ALTER TABLE flota.revision ADD CONSTRAINT fk_divisas_revision FOREIGN key (id_divisas) REFERENCES flota.divisas(id);
ALTER TABLE flota.poliza  ADD CONSTRAINT fk_seguro_poliza FOREIGN key (id_seguro) REFERENCES flota.seguro(id);
ALTER TABLE flota.grupo ADD CONSTRAINT fk_marca_grupo FOREIGN key (id_marca) REFERENCES flota.marca(id);
ALTER TABLE flota.marca ADD CONSTRAINT fk_modelo_marca FOREIGN key (id_modelo) REFERENCES flota.modelo(id);

